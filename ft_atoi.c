int	ft_atoi(const char *str)
{
	char	*s;
	int		i;
	int		sign;
	int		ret;

	s = (char *)str;
	ret = 0;
	sign = 1;
	i = 0;
	while (s[i] == '\t' || s[i] == '\v' || s[i] == '\r' || s[i] == '\f'
			|| s[i] == '\n' || s[i] == ' ')
		i++;
	if (s[i] == '+' || s[i] == '-')
	{
		if (s[i] == '-')
			sign = -1;
		i++;
	}
	while (s[i] > 47 && s[i] < 58)
	{
		ret *= 10;
		ret += str[i++] - 48;
	}
	return (sign * ret);
}
