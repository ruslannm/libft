int	ft_strcmp(const char *s1, const char *s2)
{
	char *s1_tmp;
	char *s2_tmp;

	s1_tmp = (char *)s1;
	s2_tmp = (char *)s2;
	while (*s1_tmp != '\0' && *s2_tmp != '\0' && *s1_tmp == *s2_tmp)
	{
		s1_tmp++;
		s2_tmp++;
	}
	return (*s1_tmp - *s2_tmp);
}
